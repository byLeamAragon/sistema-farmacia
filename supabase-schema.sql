create or replace function public.next_product_code()
returns text
language plpgsql
as $$
begin
  return 'FO-MED-' || lpad(nextval('public.product_code_seq')::text, 6, '0');
end;
$$;

create or replace function public.next_sale_code()
returns text
language plpgsql
as $$
begin
  return 'FO-VTA-' || to_char(now(), 'YYYY') || '-' || lpad(nextval('public.sale_code_seq')::text, 6, '0');
end;
$$;

