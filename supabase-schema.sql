create or replace function public.register_inventory_movement(
  p_product_id bigint,
  p_movement_type text,
  p_quantity integer,
  p_reason text,
  p_user_id uuid
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  current_stock integer;
  next_stock integer;
begin
  if p_quantity <= 0 then
    raise exception 'La cantidad debe ser mayor que cero';
  end if;

  select stock_quantity into current_stock
  from public.products
  where id = p_product_id
  for update;

  if current_stock is null then
    raise exception 'Producto no encontrado';
  end if;

  if p_movement_type = 'entry' then
    next_stock := current_stock + p_quantity;
  elsif p_movement_type in ('exit', 'adjustment') then
    next_stock := current_stock - p_quantity;
  else
    raise exception 'Tipo de movimiento invalido';
  end if;

  if next_stock < 0 then
    raise exception 'Stock insuficiente. Disponible: %', current_stock;
  end if;

  update public.products set stock_quantity = next_stock where id = p_product_id;

  insert into public.inventory_movements(product_id, movement_type, quantity, reason, user_id)
  values (p_product_id, p_movement_type, p_quantity, p_reason, p_user_id);
end;
$$;

create or replace function public.register_sale(
  p_customer_name text,
  p_payment_method text,
  p_user_id uuid,
  p_items jsonb
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  new_sale_id bigint;
  item jsonb;
  product_stock integer;
  item_product_id bigint;
  item_quantity integer;
  item_price numeric(12, 2);
  sale_total numeric(12, 2) := 0;
begin
  if jsonb_array_length(p_items) = 0 then
    raise exception 'La venta no tiene productos';
  end if;

  insert into public.sales(customer_name, payment_method, total, user_id)
  values (p_customer_name, p_payment_method, 0, p_user_id)
  returning id into new_sale_id;

  for item in select * from jsonb_array_elements(p_items)
  loop
    item_product_id := (item->>'product_id')::bigint;
    item_quantity := (item->>'quantity')::integer;
    item_price := (item->>'unit_price')::numeric(12, 2);

    if item_quantity <= 0 then
      raise exception 'La cantidad debe ser mayor que cero';
    end if;

    select stock_quantity into product_stock
    from public.products
    where id = item_product_id
    for update;

    if product_stock is null then
      raise exception 'Producto no encontrado';
    end if;

    if product_stock < item_quantity then
      raise exception 'Stock insuficiente para el producto %. Disponible: %', item_product_id, product_stock;
    end if;

    update public.products
    set stock_quantity = stock_quantity - item_quantity
    where id = item_product_id;

    insert into public.sale_items(sale_id, product_id, quantity, price, unit_price)
    values (new_sale_id, item_product_id, item_quantity, item_price, item_price);

    insert into public.inventory_movements(product_id, movement_type, quantity, reason, sale_id, user_id)
    values (item_product_id, 'exit', item_quantity, 'Venta', new_sale_id, p_user_id);

    sale_total := sale_total + (item_quantity * item_price);
  end loop;

  update public.sales set total = sale_total where id = new_sale_id;

  return new_sale_id;
end;
$$;

