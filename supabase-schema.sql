create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  sku text unique,
  barcode text,
  price numeric(12, 2) not null check (price >= 0),
  cost numeric(12, 2) check (cost is null or cost >= 0),
  type text not null,
  unit_id bigint references public.units(id) on delete set null,
  presentation_id bigint references public.presentations(id) on delete set null,
  location text not null,
  description text not null,
  dose text,
  stock_quantity integer not null default 0 check (stock_quantity >= 0),
  min_stock integer not null default 10 check (min_stock >= 0),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.sales (
  id bigint generated by default as identity primary key,
  sale_code text unique,
  customer_name text,
  payment_method text not null default 'efectivo',
  total numeric(12, 2) not null check (total >= 0),
  user_id uuid references auth.users(id),
  created_at timestamptz not null default now()
);

create table if not exists public.sale_items (
  id bigint generated by default as identity primary key,
  sale_id bigint not null references public.sales(id) on delete cascade,
  product_id bigint not null references public.products(id),
  quantity integer not null check (quantity > 0),
  unit_price numeric(12, 2) not null check (unit_price >= 0),
  subtotal numeric(12, 2) generated always as (quantity * unit_price) stored
);

create table if not exists public.inventory_movements (
  id bigint generated by default as identity primary key,
  movement_code text unique,
  product_id bigint not null references public.products(id),
  movement_type text not null check (movement_type in ('entry', 'exit', 'adjustment')),
  quantity integer not null check (quantity > 0),
  reason text,
  sale_id bigint references public.sales(id) on delete set null,
  user_id uuid references auth.users(id),
  created_at timestamptz not null default now()
);

